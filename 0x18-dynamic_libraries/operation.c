#include "operation.h"

/**
 * mul - sum two numbers
 * @a: first number
 * @b: second number
 * Return: returns result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * mul - substract two numbers
 * @a: first number
 * @b: second number
 * Return: returns result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: returns result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 * Return: returns result
 */
int div(int a, int b)
{
	return (a/b);
}


/**
 * mod - modulo operation
 * @a: first number
 * @b: second number
 * Return: returns result
 */
int mod(int a, int b)
{
	return (a%b);
}
